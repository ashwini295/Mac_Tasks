package Octex_10102024_Constants_Literas;

public class Lab032
{
    public static void main(String[] args) {
        boolean is_married =true;// boolean litral
        System.out.println(is_married);

        int a=100;
        System.out.println(a);// integral litral

        float f1=10.10f;
        System.out.println(f1);// floar litral

        long l1=10000000000l;
        System.out.println(l1);//long litral

        short s=90;
        System.out.println(s);// short litral

        double d=9999.00009;
        System.out.println(d);// double litral

        //in java there are 6 types of litrals present

    }
}
