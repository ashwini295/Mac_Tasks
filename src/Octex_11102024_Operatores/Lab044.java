package Octex_11102024_Operatores;

public class Lab044
{
    public static void main(String[] args) {
        //&&, ||, ! these are the logical operators

        boolean a=true;
        boolean b= false;
        boolean c= a&&b;
        System.out.println(c);

        boolean d= a||b;
        System.out.println(d);

        System.out.println(!a);
        System.out.println(!b);
        System.out.println(!!a);
    }
}
